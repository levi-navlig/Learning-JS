// Definindo as variáveis
let x = 100;
let z = 50;

// Operadores básicos

console.log(x + z);
console.log(x - z);
console.log(x / z);
console.log(x * z);

// Operadores "complexos"

console.log(x % z);
console.log(x ++);
console.log(z --);
console.log(x += z);
console.log(x -= z);
console.log(x *= z);
console.log(x /= z);
console.log(x %= z);

// Operadores de comparação

console.log(x > z);
console.log(x <= z);
console.log(x == z);
console.log(x != z);

// Operadores Lógicos


console.log(true && true);
console.log(false || true);
console.log(! true);